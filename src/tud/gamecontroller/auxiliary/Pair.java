package tud.gamecontroller.auxiliary;

public class Pair<Left, Right> {
	
	private Left left;
	private Right right;

	public Pair(Left left, Right right) {
		setLeft(left);
		setRight(right);
	}

	public void setLeft(Left left) {
		this.left = left;
	}

	public Left getLeft() {
		return left;
	}

	public void setRight(Right right) {
		this.right = right;
	}

	public Right getRight() {
		return right;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((left == null) ? 0 : left.hashCode());
		result = prime * result + ((right == null) ? 0 : right.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Pair<?,?> other = (Pair<?,?>) obj;
		if (left == null) {
			if (other.left != null)
				return false;
		} else if (!left.equals(other.left))
			return false;
		if (right == null) {
			if (other.right != null)
				return false;
		} else if (!right.equals(other.right))
			return false;
		return true;
	}

}
